/*  Реализовать основные 4 арифметические операции в виде функций с двумя параметрами. Обязательно использовать
оператор return. */

function sum(a, b) {
    return a + b
}

function difference(a, b) {
    return a - b
}

function multiply(a, b) {
    return a * b
}

function divide(a, b) {
    return a / b
}

